#define	GOLD_MAX		999999
#define	GOLD_KETA_MAX	6

//-------------------------------------------------------------------------
//								所持金チェック
//	引数  ：my    = チェック場所
//			check = チェック金額
//	戻り値：TRUE  = 足りている
//			FALSE = 足りない
//-------------------------------------------------------------------------
extern	u8 CheckGold( u32 my, u32 check );

//-------------------------------------------------------------------------
//								所持金を増やす
//	引数：*my = 増やす場所
//		  add = 増やす金額
//-------------------------------------------------------------------------
extern	void AddGold( u32 *my, u32 add );

//-------------------------------------------------------------------------
//								所持金を減らす
//	引数：*my = 減らす場所
//		  sub = 減らす金額
//-------------------------------------------------------------------------
extern	void SubGold( u32 *my, u32 sub );

//-------------------------------------------------------------------------
//								金額表示
//	引数：my   = 金額
//		  keta = 表示桁数
//		  x,y  = ウィンドウ左上座標
//-------------------------------------------------------------------------
extern	void DrawGold( u32 my, u8 keta, u8 x, u8 y );

//-------------------------------------------------------------------------
//						ウィンドウ内の所持金表示
//	引数：my  = 所持金
//		  x,y = ウィンドウ左上座標
//-------------------------------------------------------------------------
extern	void DrawWinOkozukai( u32 my, u8 x, u8 y );

//-------------------------------------------------------------------------
//							所持金ウィンドウ表示
//	引数：my  = 所持金
//		  x,y = ウィンドウ左上座標
//-------------------------------------------------------------------------
extern	void DrawGoldWin( u32 my, u8 x, u8 y );

//-------------------------------------------------------------------------
//							所持金ウィンドウ削除
//	引数：x,y = ウィンドウ左上座標
//-------------------------------------------------------------------------
extern	void DelOkozukaiWin( u8 x, u8 y );
