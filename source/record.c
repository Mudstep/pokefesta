#include "common.h"

#define	__RECORD_H_GLOBAL__
#include "record.h"


#if ( CNT_END > SV_RECORD_MAX )
#error "レコード項目の定義が多すぎ！"
#endif

//=========================================================================
//	定義
//=========================================================================
//#define name value

enum{
	//レコードのカウント最大値（u24として決めている）
	RECORD_VALUE_MAX = 0xffffff,
};


//=========================================================================
//	変数
//=========================================================================
//GLOBAL type valiable;

//=========================================================================
//	関数プロトタイプ
//=========================================================================
//GLOBAL type function(arg...);

//-------------------------------------------------------------------------
//-------------------------------------------------------------------------
void RecordInit( void )
{
	int i;
	for( i=0; i< SV_RECORD_MAX; i++ ) Fld.Record[i] = 0;
}

//-------------------------------------------------------------------------
//	カウンタのインクリメント
//-------------------------------------------------------------------------
void IncRecord( u8 id )
{
	if ( id >= CNT_END ) return;

	if (Fld.Record[id] < RECORD_VALUE_MAX) {
		Fld.Record[id] ++;
	} else {
		Fld.Record[id] = RECORD_VALUE_MAX;
	}
}

//-------------------------------------------------------------------------
//	カウンタの取得
//-------------------------------------------------------------------------
u32 GetRecord( u8 id )
{
	if ( id >= CNT_END ) return 0;
	return Fld.Record[id];
}

//-------------------------------------------------------------------------
//	カウンタのセット
//-------------------------------------------------------------------------
void SetRecord( u8 id, u32 value )
{
	if ( id >= CNT_END ) return;
	Fld.Record[id] = value;
}


