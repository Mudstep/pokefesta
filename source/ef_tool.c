//----------------------------------------------------------
//	ソフトフェードイン、アウト
// start_pal = 何本目のﾊﾟﾚｯﾄから色を変えるか(0〜255)
// end_pal = start_palから何本のﾊﾟﾚｯﾄを変更の対象とするか(1〜256(256色｜16色*16本))
// evy = 係数(0〜16) (変更の度合い、0(元の色)〜2,3..(中間色)〜16(指定した色)
// next_rgb = 変更後の色を指定
//----------------------------------------------------------
#include "softfade.h"

void SoftFade(u16 start_pal, u16 end_pal, u8 evy, u16 next_rgb)
{
	u16 i;
	u16 offset;
	s8 red,green, blue;

	for(i = 0; i < end_pal; i++)
	{
		offset = i + start_pal;

		red = ((PlttData*)&PaletteWork[offset])->Red;
		green = ((PlttData*)&PaletteWork[offset])->Green;
		blue = ((PlttData*)&PaletteWork[offset])->Blue;
		PaletteWorkTrans[offset] = 
			FADE_CHENJI(red, ((PlttData*)&next_rgb)->Red, evy) |
			(FADE_CHENJI(green, ((PlttData*)&next_rgb)->Green, evy) << 5) |
			(FADE_CHENJI(blue, ((PlttData*)&next_rgb)->Blue, evy) << 10);
	}
}




