//-------------------------------------------------------------------------
//	マップアトリビュートチェック：強制移動地形かどうか
//	<返値>	TRUE	そうです
//			FALSE	それ以外
//-------------------------------------------------------------------------
u8 ATR_IsForceMoveCheck(u8 attr)
{
	if (
			( 0x40 <= attr && attr <= 0x48 )	//強制移動（陸上）
			|| ( 0x50<=attr && attr<=0x53 )		//強制移動（水上）
			|| attr == 0xd0						//すべる坂
			|| attr == 0xd2						//壊れる床
			|| attr == 0x13						//滝
			|| attr == 0x20						//氷床
			|| attr == 0xbb						//ジャンピングマット
			|| attr == 0xbc						//くるっとマット
			)
	{
		return TRUE;
	} else {
		return FALSE;
	}
}
